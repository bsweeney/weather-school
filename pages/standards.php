<h4 style="margin:auto; font-size:135%; text-align: center;">NGSS Disciplinary Core Ideas</h4>
<h5 style="font-size:120%; margin-bottom:0em;">MS-ESS2.D: Weather and Climate</h5>
<ul style="font-size:115%">
<li>Weather and climate are influenced by interactions involving sunlight, the ocean, the atmosphere, <span style="color:gray;">ice,</span>* landforms<span style="color:gray;">, and living things</span>. These interactions vary with latitude, altitude, and local and regional geography<span style="color:gray;">, all of which can affect oceanic and atmospheric flow patterns</span>.</li>
<span style="color:gray;"><li>Because these patterns are so complex, weather can only be predicted probabilistically.</li></span>
<li>The ocean exerts a major influence on weather and climate by absorbing energy from the sun, releasing it over time<span style="color:gray;">, and globally redistributing it through ocean currents</span>. (NGSS Standards, p. 82)</li>
</ul>

<h4 style="margin:2.25em auto; font-size:135%; text-align: center;">NGSS Scientific Practices</h4>
<h5 style="font-size:120%; margin-bottom:0em;">Practice 1: Asking Questions and Defining Problems </h5>
<p style="font-size:115%; margin-left:2em;">Ask questions</p>
<ul style="font-size:115%">
<li>that arise from careful observation of phenomena, models, or unexpected results, to clarify and/or seek additional information.</li>
<li>to determine relationships between independent and dependent variables and relationship in models. (NGSS Appendixes, p. 51)</li>
</ul>
<h5 style="font-size:120%; margin-bottom:0em;">Practice 3: Planning and Carrying out Investigations</h5>
<ul style="font-size:115%">
<li>Collect data <span style="color:gray;">to produce data</span> to serve as the basis for evidence to answer scientific questions <span style="color:gray;">or to test design solutions under a range of conditions</span>. (NGSS Appendixes, p. 55)</ul>
</ul>

<h5 style="font-size:120%; margin-bottom:0em;">Practice 4: Analyzing and Interpreting Data</h5> 
<ul style="font-size:115%">
<li>Construct, analyze, and/or interpret graphical displays of data and /or large data sets to identify linear and non-linear relationships.</li>
<li>Use graphical displays (e.g. maps, charts, graphs, and/or tables) of large data sets to identify temporal and spatial relationships.</li>
<li>Analyze and interpret data to determine similarities and differences in findings. (NGSS Appendixes, p. 57)</li>
</ul>

<h5 style="font-size:120%; margin-bottom:0em;">Practice 5: Using Mathematics and Computational Thinking</h5>
<ul style="font-size:115%">
<li>Use digital tools (computers) to analyze very large data sets for patterns and trends. (NGSS, Volume 2, Appendixes, p. 59)</li>
</ul>

<h4 style="margin:2.25em auto; font-size:135%; text-align: center;">NGSS Crosscutting Concepts</h4>
<h5 style="font-size:120%; margin-bottom:0em;">Patterns</h5>
<ul style="font-size:115%">
<li>Patterns in rates of change and other numerical relationships can provide information about natural systems. (NGSS Standards, p. 81)</li>
</ul>

<h5 style="font-size:120%; margin-bottom:0em;">Cause and Effect</h5>
<ul style="font-size:115%">
<li>Cause and effect relationships may be used to predict phenomena in natural systems. (NGSS Standards, p. 81)</li>
Patterns in rates of change and other numerical relationships can provide information about natural systems. (NGSS Standards, p. 81)</li>
<li>Students] also understand that phenomena may have more than one cause<span style="color:gray;">, and some cause and effect relationship in systems can only be described using probability</span>. (NGSS Appendixes, p. 83)</li>
</ul>

<h5 style="font-size:120%; margin-bottom:0em;">Scale, Proportion, and Quantity</h5>
<ul style="font-size:115%">
<li>Time, space, and energy phenomena can be observed at various scales using models to study systems that are too large <span style="color:gray;">or too small</span>. (NGSS Standards, p. 81)</li>
</ul>

<h5 style="font-size:120%; margin-bottom:0em;">Stability and Change</h5>
<ul style="font-size:115%">
<li>Explanations of stability and change in natural or designed systems can be constructed by examining the changes over time <span style="color:gray;">and processes at different scales, including the atomic scale</span>. (NGSS Standards, p. 81)</li>
</ul>
