<h2 style="text-align:center; font-size:175%;">About WeatherSchool</h2>
<p style="margin:auto;font-size:125%">
<br>
WeatherSchool @ AAAS introduces you to a variety of factors that affect patterns in air temperature. In a series of modules, you will learn how moving air masses can cause day-to-day temperature variation, how geographic factors such as elevation above sea level can influence temperature at any given location, and how the movement of the earth in relation to the sun can affect temperature over the course of a year. The modules use Google Maps to interact with daily measurements collected by the National Oceanographic and Atmospheric Administration (NOAA). The data were collected by local weather stations between 1929 and 2010 in the United States and around the world.</p>
<p style="margin:auto;font-size:125%">
<br>
Each module begins with a guided activity designed to illustrate a temperature pattern or a variable affecting that pattern. This is followed by an open-ended <b>Try It</b> activity. In the <b>Try It</b> activity, you can further explore the featured pattern by picking different locations on the maps and choosing different dates.</p>
<p style="margin:auto;font-size:125%">
<br>
At the end of each module you can check your understanding by answering the <b>Quiz Yourself</b> questions or learn more by following the <b>Expand Your Knowledge</b> links.</p>
